public class PerbandinganTrueFalse{
	public static void main(String[] args) {
		int a=10;
		int b=8;
		int c=12;
		int d=5;
		System.out.println("Tes ke 1 = " + (a>b));
		System.out.println("Tes ke 2 = " + (b<c));
		System.out.println("Tes ke 3 = " + (a>=d));
		System.out.println("Tes ke 4 = " + (d<=b));
		System.out.println("Tes ke 5 = " + (a+a==b+c));
		System.out.println("Tes ke 6 = " + (b!=d));
		System.out.println("Tes ke 7 = " + (b>c));
		System.out.println("Tes ke 8 = " + (a<=b));
		System.out.println("Tes ke 9 = " + (a==b));
		System.out.println("Tes ke 10 = "+ ((a+a)!=b+c));
	}
}